""" This is the increment function"""


class Calculator:
    """ This is the Calculator class """

    @staticmethod
    def add_number(value_a, value_b):
        """ adds number """
        total = value_a + value_b
        return total

    @staticmethod
    def subtract_number(value_a, value_b):
        """ subtract number """
        total = value_a - value_b
        return total

    @staticmethod
    def multiply_number(value_a, value_b):
        """ multiply number """
        total = value_a * value_b
        return total
